from ehtplot.color import vis as ev

def plot_pyramid(ax, cmap):
    ax.imshow(ev.pyramid(), cmap=cmap, vmin=0.0, vmax=1.0)
    ax.set_xticks([])
    ax.set_xticklabels([])
    ax.set_yticks([])
    ax.set_yticklabels([])
    ax.set_title(cmap)
